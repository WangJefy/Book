package book.net.simplesocket;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.OutputStream;
import java.io.PrintStream;
import java.net.Socket;
import java.net.UnknownHostException;

/*
 * 一个简单的socket客户端，能够往服务器发送socket请求
 */
public class SimpleClient {
	//客户端输入输出流
	PrintStream out;
	BufferedReader in;
	
	public SimpleClient(String serverName,int port) throws IOException {
		//根据服务器名和端口号，连接服务器
		Socket clientSocket = new Socket(serverName,port);
		try {
			//获取socket的输入输出流
			OutputStream outStream = clientSocket.getOutputStream();
			out = new PrintStream(outStream);
			in = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
		} catch (UnknownHostException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
			System.out.println("无法连接服务器");
			
			clientSocket.close();
			out.close();
			in.close();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}
	//发送请求
	public void sendRequest(String request) {
		out.println(request);//向socket的输出流写数据
		System.out.println("Client 发送请求：" + request);
	}
	
	public String getResponse() {
		String str = null;
		try {
			str = in.readLine();
			System.out.println("Client 收到Server返回：" + str);
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}//从socket输入流中写数据
		return str;
	}
}
